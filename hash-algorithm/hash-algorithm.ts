// 哈希算法

/**
 * 哈希算法的原理：将任意长度的二进制值映射为固定长度的二进制值串，这个映射规则就称为"哈希算法"，该二进制串称为"哈希值"
 */

/**
 * 哈希算法的要求：
 * 1. 从哈希值不能反向推导出原始数据（因此，哈希算法也称为单向哈希算法）
 * 2. 对数据变化敏感。哪怕原始数据只改动一个二进制位，对应的哈希值也大不相同
 * 3. 哈希冲突的概率要很小
 * 4. 执行效率要高，对较长的文本，也能快速计算出哈希值
 */

/**
 * 哈希算法的应用：
 * 1. 安全加密
 * 2. 唯一标识
 * 3. 数据校验
 * 4. 哈希函数
 * 5. 负载均衡
 * 6. 数据分片
 * 7. 分布式存储
 */
